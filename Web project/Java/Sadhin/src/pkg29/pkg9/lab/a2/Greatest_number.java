public class Greatest_number {

  
    public static void main(String[] args)
    {
        int a=15, b=20, c=4;
       
       
        if(a > b && a > c)
        {
            System.out.println("Largest number is:"+a);
        }
        else if(b > c)
        {
            System.out.println("Largest number is:"+b);
        }
        else
        {
            System.out.println("Largest number is:"+c);
        }
 
    }
}
